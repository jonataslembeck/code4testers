def maior_que(v1, v2)
  puts v1 > v2
end

def menor_que(v1, v2)
  puts v1 < v2
end

def maior_ou_igual_que(v1, v2)
  puts v1 >= v2
end

def menor_ou_igual_que(v1, v2)
  puts v1 <= v2
end

def igual(v1, v2)
  puts v1 == v2
end

def diferente(v1, v2)
  puts v1 != v2
end

